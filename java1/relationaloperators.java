public class relationaloperators {
    public static void main(String[] args) {     
           //Relational operators will return true or false.  

        int a= 2, b=3;

        System.out.println(a + " " + b);

        System.out.println(a == b);

        System.out.println(a != b );

        System.out.println(a>b);

        System.out.println(a<b);

        System.out.println(a<=b);

        System.out.println(a>=b);
    }
    
}
